#include <stdio.h> // This line includes the standard input/output library, which provides functions like printf.

int main(){ // This line defines the main function, the entry point of every C program.

  printf("Hello, World!\n"); // This line uses the printf function to print the string "Hello, World!" to the console. The \n character adds a newline, moving the cursor to the next line.

  return 0; // This line returns 0 to the operating system, indicating that the program executed successfully.
}
