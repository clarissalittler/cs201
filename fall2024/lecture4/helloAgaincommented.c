#include <stdio.h>  // This line includes the standard input/output library (stdio.h). 
                    // It provides functions like printf() for printing to the console.

int main(){  // This is the main function, the entry point of the program. 
              // Every C program must have a main function. 
              // The `int` specifies the return type of the function, meaning it will return an integer value.

  printf("Hello, World");  // This line uses the printf() function from stdio.h to print the string "Hello, World" to the console.

  return 0;  // This line returns the value 0 from the main function. 
              // In C, a return value of 0 typically indicates that the program executed successfully.
} 
