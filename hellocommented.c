#include <stdio.h> 

// This line includes the standard input/output library (stdio.h).
// This library provides functions for interacting with the user, 
// such as printing text to the screen (using printf) and reading input.

int main(){ 
  // This line defines the main function. The main function is the 
  // entry point of every C program. Execution begins here. 

  printf("Hello, World!\n"); 
  // This line calls the printf function. printf is used to print text to the console.
  // The text to be printed is enclosed in double quotes.
  // The special character '\n' represents a newline character, which moves the cursor to the next line.

  return 0;
  // This line returns a value of 0 to the operating system.
  // In C, a return value of 0 typically indicates that the program executed successfully.
}
